let newListIP = {}; // new Object();
let finalTable = {}; // таблица для ответа
let uniqueIP = 0; // колличество уникальных адресов
let singleVisit = 0; // колличество адресов с единичным посещением

// создаём ассоциативный массив
// где ключ это IP, а значение это частота посещения
// при первом добавлении считаем уникальных посетителей
// при повторе увеличиваем частоту(значение)
IPs.forEach((el) => {
if (el.ip in newListIP) {
  newListIP[el.ip] += 1;
} else {
  newListIP[el.ip] = 1;
  uniqueIP += 1;
}});

// создаём массив состоящий только из отсортированных IP адрессов
// сортировка по убыванию посещения, сверху вниз
const sortedIP = (obj => {
    let keys = [];
    for(let key in obj) {
      keys.push(key);
    };
    return keys.sort((a,b) => {return obj[b]-obj[a];});
})(newListIP);

// добавляем в финальную таблицу IP из отсортированного, а частоту из массива со значениями
// считаем одиночных посетителей
sortedIP.forEach((el) => {
  finalTable[el] = newListIP[el];
  if (newListIP[el] === 1) {
    singleVisit += 1;
  };
});

console.log(finalTable); // частотная, отсортированная по убыванию таблица
